//    obsStatus   → SDMX OBS_STATUS ('A' actual | 'P' preliminary)
//
//  M (Imports): stored as POSITIVE.
//  Pipe transform negates it: C + I_GFCF + X − M = GDP.
//
//  Migration: swap GDP_FACTS for a live API fetch — zero other changes.
//

